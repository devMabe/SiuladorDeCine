    public void actualizarPelicula(){
        
        for(int i =0; i < opPeliculas.getItemCount();i++){          
            if(opPeliculas.getItemAt(i).equals(pla)){
                opPeliculas.setSelectedIndex(i);  
                try{
                    if(file.exists()){
                        fileBr = new BufferedReader(new FileReader(file));
                        String line, lineOb;
                        while(( line = fileBr.readLine())!=null){
                            lineOb = obtenerParte(line, 1);
                            if(lineOb.equals(pla)){
                                String src = obtenerParte(line,2);
                                cambioP.removeAll();
                                JPanel p = new FondoPanel(src);
                                p.setSize(new Dimension(cambioP.getWidth(),cambioP.getHeight()));
                                p.setPreferredSize(new Dimension(cambioP.getWidth(),cambioP.getHeight()));
                                p.setVisible(true);
                                cambioP.add(p);
                                panelPelicula.setBackground(Color.BLACK);
                                panelPelicula.updateUI();
                                nombrePelicula.setText(pla);
                                vPelicula.setText(obtenerParte(line,3));
                                sinopsis.setText(obtenerParte(line,4));
                            }
                            
                        }
                        fileBr.close();
                    }
                }catch(Exception e){
                    e.printStackTrace();
                } 
            }
        }
    }
    public void agregarComboBox(File file, JComboBox com, int n){
        try{
            String r ="";
            if(file.exists()){
                fileBr = new BufferedReader(new FileReader(file));
                String line, lineOb;
                while(( line = fileBr.readLine())!=null){
                    lineOb = obtenerParte(line, n);
                    if(!r.equals(lineOb)){
                        com.addItem(lineOb);
                    }
                    r = lineOb; 
                }
                fileBr.close();
                
            }
        }catch(Exception e){
            e.printStackTrace();
        }  
    }
    
    public void actualizarComboBox(File file, JComboBox com, String r){
        try{
            
            if(file.exists()){
                com.removeAllItems();
                fileBr = new BufferedReader(new FileReader(file));
                String line, lineOb;
                while(( line = fileBr.readLine())!=null){
                    lineOb = obtenerParte(line, 1);
                    if(r.equals(lineOb)){
                        com.addItem(obtenerParte(line,2));
                    } 
                }
                fileBr.close();
                
            }
        }catch(Exception e){
            e.printStackTrace();
        } 
    }
    
    public String obtenerParte(String line, int n){
        int i=0, k = 0, h = 0;
        while(k != n){
            if(i == 0){
                h = i;
            }else{
                h = i+1;
            }        
            i++;
            while(!line.substring(i, i+1).equals(";"))i++;
            
            k++;       
        }    
        //System.out.println("IdLine: "+ line.substring(h, i));
        return line.substring(h,i);
    }
    
    
