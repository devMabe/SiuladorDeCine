    public void actualizarRanking(CompraPeli c){
        Iterator it = ranking.iterator();
        while(it.hasNext()){
            Ranking rk = (Ranking)it.next();
            if(rk.getNombre().equals(c.getNombre())){
                rk.setCantidad(rk.getCantidad()+c.getCantidad());
            }
        }
    }
    public void calcularTotal(){
        double valor = Double.parseDouble(vPelicula.getText());
        double cantidad = Double.parseDouble(vCantidad.getText());
        double incremento = Double.parseDouble(vIncremento.getText());
        double total = (valor * cantidad) +incremento;
        vTotal.setText(""+total);
    }
